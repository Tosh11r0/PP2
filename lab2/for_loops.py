# Python - Циклы for

# Цикл for используется для перебора последовательностей (списков, кортежей, словарей, множеств или строк).

# Пример: Перебор списка фруктов
fruits = ["apple", "banana", "cherry"]
for x in fruits:
    print(x)
# Вывод:
# apple
# banana
# cherry

# Перебор строки
# Строки являются итерируемыми объектами, они содержат последовательность символов.

# Пример: Перебор букв в слове "banana"
for x in "banana":
    print(x)
# Вывод:
# b
# a
# n
# a
# n
# a

# Оператор break
# С помощью оператора break можно прервать цикл до его завершения.

# Пример: Прерывание цикла при достижении "banana"
fruits = ["apple", "banana", "cherry"]
for x in fruits:
    if x == "banana":
        break
    print(x)
# Вывод:
# apple

# Оператор continue
# С помощью оператора continue можно пропустить текущую итерацию и перейти к следующей.

# Пример: Пропуск "banana"
fruits = ["apple", "banana", "cherry"]
for x in fruits:
    if x == "banana":
        continue
    print(x)
# Вывод:
# apple
# cherry

# Функция range()
# Функция range() возвращает последовательность чисел, начиная с 0 по умолчанию, с шагом 1 по умолчанию, и заканчивая указанным числом (не включая его).

# Пример: Использование функции range()
for x in range(6):
    print(x)
# Вывод:
# 0
# 1
# 2
# 3
# 4
# 5

# Функция range() может принимать начальное значение, конечное значение и шаг.

# Пример: Указание начального значения и шага
for x in range(2, 10, 2):
    print(x)
# Вывод:
# 2
# 4
# 6
# 8

# Оператор else в цикле for
# Оператор else в цикле for выполняется после завершения цикла, если не было использовано прерывание (break).

# Пример: Использование else в цикле for
for x in range(6):
    print(x)
else:
    print("Цикл завершен")
# Вывод:
# 0
# 1
# 2
# 3
# 4
# 5
# Цикл завершен

# Вложенные циклы
# Вложенный цикл — это цикл внутри другого цикла.

# Пример: Вложенные циклы для сочетания прилагательных и фруктов
adj = ["red", "big", "tasty"]
fruits = ["apple", "banana", "cherry"]
for x in adj:
    for y in fruits:
        print(x, y)
# Вывод:
# red apple
# red banana
# red cherry
# big apple
# big banana
# big cherry
# tasty apple
# tasty banana
# tasty cherry

# Оператор pass
# Циклы не могут быть пустыми, но если по какой-то причине у вас есть цикл без содержимого, используйте оператор pass, чтобы избежать ошибки.

# Пример: Пустой цикл с использованием pass
for x in [0, 1, 2]:
    pass
